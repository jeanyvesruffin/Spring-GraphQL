-- === Table: LEAGUE ===
CREATE TABLE LEAGUE (
    ID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    NAME VARCHAR(200) NOT NULL,
    CODE VARCHAR(50),
    COUNTRY VARCHAR(100),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP,
    PRIMARY KEY (ID)
);

CREATE UNIQUE INDEX UX_LEAGUE_CODE ON LEAGUE (CODE);

-- === Table: TEAM ===
CREATE TABLE TEAM (
    ID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    LEAGUE_ID BIGINT,
    NAME VARCHAR(200) NOT NULL,
    SHORT_NAME VARCHAR(50),
    FOUNDED_YEAR INTEGER,
    CITY VARCHAR(100),
    STADIUM VARCHAR(200),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (ID),
    CONSTRAINT FK_TEAM_LEAGUE FOREIGN KEY (LEAGUE_ID) REFERENCES LEAGUE(ID) ON DELETE SET NULL
);

CREATE UNIQUE INDEX UX_TEAM_LEAGUE_NAME ON TEAM (LEAGUE_ID, NAME);

-- === Table: PLAYER ===
CREATE TABLE PLAYER (
    ID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    TEAM_ID BIGINT,
    FIRST_NAME VARCHAR(100) NOT NULL,
    LAST_NAME VARCHAR(100) NOT NULL,
    BIRTHDATE DATE,
    POSITION VARCHAR(50),
    NUMBER INTEGER,
    NATIONALITY VARCHAR(100),
    CONTRACT_UNTIL DATE,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (ID),
    CONSTRAINT FK_PLAYER_TEAM FOREIGN KEY (TEAM_ID) REFERENCES TEAM(ID) ON DELETE SET NULL
);

CREATE INDEX IDX_PLAYER_TEAM ON PLAYER (TEAM_ID);

-- === Table: MATCHES ===
CREATE TABLE MATCHES (
    ID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    LEAGUE_ID BIGINT,
    SEASON VARCHAR(20),
    MATCH_DATE TIMESTAMP NOT NULL,
    HOME_TEAM_ID BIGINT NOT NULL,
    AWAY_TEAM_ID BIGINT NOT NULL,
    HOME_SCORE INTEGER DEFAULT 0,
    AWAY_SCORE INTEGER DEFAULT 0,
    STATUS VARCHAR(50) DEFAULT 'SCHEDULED',
    VENUE VARCHAR(200),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (ID),
    CONSTRAINT FK_MATCH_LEAGUE FOREIGN KEY (LEAGUE_ID) REFERENCES LEAGUE(ID) ON DELETE SET NULL,
    CONSTRAINT FK_MATCH_HOME_TEAM FOREIGN KEY (HOME_TEAM_ID) REFERENCES TEAM(ID) ON DELETE RESTRICT,
    CONSTRAINT FK_MATCH_AWAY_TEAM FOREIGN KEY (AWAY_TEAM_ID) REFERENCES TEAM(ID) ON DELETE RESTRICT,
    CONSTRAINT CHK_MATCH_TEAMS CHECK (HOME_TEAM_ID <> AWAY_TEAM_ID)
);

CREATE INDEX IDX_MATCH_DATE ON MATCHES (MATCH_DATE);
CREATE INDEX IDX_MATCH_TEAMS ON MATCHES (HOME_TEAM_ID, AWAY_TEAM_ID);

-- === Table: PLAYER_STATISTIC ===
CREATE TABLE PLAYER_STATISTIC (
    ID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    MATCH_ID BIGINT NOT NULL,
    PLAYER_ID BIGINT NOT NULL,
    TEAM_ID BIGINT,
    MINUTES_PLAYED INTEGER DEFAULT 0,
    GOALS INTEGER DEFAULT 0,
    ASSISTS INTEGER DEFAULT 0,
    YELLOW_CARDS INTEGER DEFAULT 0,
    RED_CARDS INTEGER DEFAULT 0,
    SHOTS_ON_TARGET INTEGER DEFAULT 0,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (ID),
    CONSTRAINT FK_STAT_MATCH FOREIGN KEY (MATCH_ID) REFERENCES MATCHES(ID) ON DELETE CASCADE,
    CONSTRAINT FK_STAT_PLAYER FOREIGN KEY (PLAYER_ID) REFERENCES PLAYER(ID) ON DELETE CASCADE
);

CREATE UNIQUE INDEX UX_STAT_MATCH_PLAYER ON PLAYER_STATISTIC (MATCH_ID, PLAYER_ID);
CREATE INDEX IDX_STAT_PLAYER ON PLAYER_STATISTIC (PLAYER_ID);

-- === Table: ACHIEVEMENT ===
CREATE TABLE ACHIEVEMENT (
    ID BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1),
    PLAYER_ID BIGINT,
    TEAM_ID BIGINT,
    TITLE VARCHAR(200) NOT NULL,
    DESCRIPTION CLOB,
    ACHIEVED_AT DATE,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (ID),
    CONSTRAINT FK_ACHIEVEMENT_PLAYER FOREIGN KEY (PLAYER_ID) REFERENCES PLAYER(ID) ON DELETE CASCADE,
    CONSTRAINT FK_ACHIEVEMENT_TEAM FOREIGN KEY (TEAM_ID) REFERENCES TEAM(ID) ON DELETE CASCADE
);

CREATE INDEX IDX_ACHIEVEMENT_PLAYER ON ACHIEVEMENT (PLAYER_ID);
CREATE INDEX IDX_ACHIEVEMENT_TEAM ON ACHIEVEMENT (TEAM_ID);

-- Fin du script
